    PUNTOS NUMBER
);

-- TABLA SALSAS
CREATE TABLE SALSA(
    NOMBRE VARCHAR(30),
    INGREDIENTES VARCHAR(200),
    -- PICOR MAXIMO LONGITUD: EXTREMO (7 CARACTERES)
    PICOR VARCHAR(7),
    RECOMENDACIONES VARCHAR(200)
);

-- TABLA RELACION: CLIENTE - COMPRAR - SALSA
CREATE TABLE COMPRAR_SALSA(
    NOMBRE VARCHAR(30),
    CURP VARCHAR(18),
    FECHA DATE,
    -- OPCION SI/NO, LA DIRECCION SE OBTIENE DEL CLIENTE
    A_DOMICILIO VARCHAR(2)
);

