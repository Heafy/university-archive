--NO OLVIDAR EL CLIENTE DEFAULT
-- TABLA CLIENTE
CREATE TABLE CLIENTE(
    NOMBRE VARCHAR(25),
    AP_PATERNO VARCHAR(25),
    AP_MATERNO VARCHAR(25),
    SEXO VARCHAR(9),
    CALLE VARCHAR(50),
    NUMERO_EXT NUMBER,
    NUMERO_INT NUMBER,
    COLONIA VARCHAR(50),
    ESTADO VARCHAR(30),
    CP NUMBER(5,0),
    TELEFONO NUMBER(10),
    EMAIL VARCHAR(80),
    CURP VARCHAR(18)
);

